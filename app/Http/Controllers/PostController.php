    {
      $validated = request()->validate([
        'firstName' => 'required|min:3',
        'secondName' => 'required|min:3',
        'date_of_birth' => 'required',
        'gender' => 'required|min:4',
        'phoneNumber' => 'required|min:10'
      ]);

        Post::create($validated);

        return redirect()->back();
    }

